package sampleJavaPackage;

public class Adder {

		void sum(int x,int y)
		{
			System.out.println(x+y);
		}
		
		void sum(int x,int y, int z)
		{
			System.out.println(x+y+z);
		}
		
		void sum(int x, double y)
		{
			System.out.println(x+y);
		}
		
		void sum(double x,int y)
		{
			System.out.println(x+y);
		}
}
