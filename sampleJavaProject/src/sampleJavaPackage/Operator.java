package sampleJavaPackage;

public class Operator {

	public static void main(String[] args) {

		// Incremental 
		int i=10;
		int res=i++;
		System.out.println(res);
		System.out.println(i);
		
		int a=10;
		int res1=++a;
		System.out.println(res1);
		System.out.println(a);
		
		
		//  Ternary operator 
		int c=300,d=200;
		int e=(c>d)?c:d;
		
		System.out.println(e);
		
	}

}
